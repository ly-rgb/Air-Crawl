def get_cn_phone():
    ret = '13'
    for _ in range(9):
        ret += str(random.randint(0, 9))
    return ret


def random_phone(type_coed="CN", phone_len=None) -> tuple:
    if type_coed == "CN":
        return "86", get_cn_phone()
    phone_len = phone_len or __phone_len__.get(type_coed, None)
    if phone_len is None:
        return "86", get_cn_phone()
    area_code = random.choice(list(__code_map__.get(type_coed, ["", ])))
    phone = ""
    first_num = str(random.randint(1, 9))
    phone += first_num
    for _ in range(phone_len-1):
        phone += str(random.randint(0, 9))
    return area_code, phone


